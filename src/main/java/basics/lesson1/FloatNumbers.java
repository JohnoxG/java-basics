package basics.lesson1;

public class FloatNumbers {

	public static void main(String[] args) {
		float singlePrecisionNumber = 10.5f;
		double doublePrecisionNumber = 10.25;
		double doublePrecisionNumberWihSuffix = 109.25d;

		System.out.println("floating point number : " + singlePrecisionNumber + ", double float: " + doublePrecisionNumber
				+ ", double float with 'd' suffix: " + doublePrecisionNumberWihSuffix);
	}
}
